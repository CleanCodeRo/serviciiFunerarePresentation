"use client";

import Preview from "@/components/preview/Preview";
import { useEffect, useState } from "react";

const ModalProvider = () => {
  const [isMounted, setIsMounted] = useState(false);

  useEffect(() => {
    setIsMounted(true);
  }, []);

  if (!isMounted) return null;

  return (
    <>
      <Preview />
    </>
  );
};

export default ModalProvider;
